    private void txteditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txteditarActionPerformed
        Tabelaclientes ce = new Tabelaclientes();
        TabelaclientesDAO cedao = new TabelaclientesDAO();
        
        int linhaSelecionada = jTConsulta.getSelectedRow();
        
        String idCliente = jTConsulta.getModel().getValueAt(linhaSelecionada, 0).toString();
        int idClienteI = Integer.parseInt(idCliente);
        
        ce.setIdclientes(idClienteI);
        
        cedao.readEditar(ce);
        
        Consulta.setVisible(false);
        Editar.setVisible(true);
        
        txtNomecli.setText(ce.getNome());
        txtcpf.setText(ce.getCpf());
        txtcnpj.setText(ce.getCNPJ());
        txtrg.setText(ce.getRG());
        txtcel.setText(ce.getCel());
        txtfixo.setText(ce.getFixo());
        txtemail.setText(ce.getEmail());
        txtrua.setText(ce.getRua());
        txtn.setText(String.valueOf(ce.getNumerocasa()));
        txtcep.setText(ce.getCEP());
        txtbairro.setText(ce.getBairro());
        txtcidade.setText(ce.getCidade());
        txtcomplem.setText(ce.getComplemento());
        txtid.setText(String.valueOf(ce.getIdclientes()));
        
    }//GEN-LAST:event_txteditarActionPerformed

