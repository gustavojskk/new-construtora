
package model.bean;


public class orcamento {
    
    private String materiais;
    private String quantidade;
    private String servico;
    private String prazodeobra;
    private String total;
    private String idclientes;

    public String getIdclientes() {
        return idclientes;
    }

    public void setIdclientes(String idclientes) {
        this.idclientes = idclientes;
    }

    public String getMateriais() {
        return materiais;
    }

    public void setMateriais(String materiais) {
        this.materiais = materiais;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    public String getServico() {
        return servico;
    }

    public void setServico(String servico) {
        this.servico = servico;
    }

    public String getPrazodeobra() {
        return prazodeobra;
    }

    public void setPrazodeobra(String prazodeobra) {
        this.prazodeobra = prazodeobra;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
    

    
}
